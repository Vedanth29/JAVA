public class Pattern {
    public static void main(String[] args) {
        int i,j,rows=6;
        for(i=0;i<rows;i++)
        {
            //System.out.print("*");
            for(j=0;j<=i;j++)
            {
                System.out.println("* ");
            }
            System.out.println();
        }
    }
}
