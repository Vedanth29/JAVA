public class Ascii_value {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 'a';
        int Ascii_value = ch1;
        int ascii_value = ch2;

        System.out.println("the ascii value of "+ch1+" is "+Ascii_value);
        System.out.println("the ascii value of "+ch2+" is "+ascii_value);


    }
}
